    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        try{
            conn con = new conn();
            if(evt.getSource()==search){
                String sql = "select * from book where idBook=?";
                PreparedStatement st = con.c.prepareStatement(sql);
                st.setString(1,b_Id.getText());
                ResultSet rs = st.executeQuery();
                while(rs.next()){
                    b_name.setText(rs.getString("bookname"));
                    b_ISBN.setText(rs.getString("isbn"));
                    b_pub.setText(rs.getString("publisher"));
                    b_price.setText(rs.getString("price"));
                    b_pages.setText(rs.getString("pages"));
                }
                st.close();
                rs.close();
            }
        }
        catch(Exception e){
            System.out.println("Error!");
        }
    }//GEN-LAST:event_searchActionPerformed

